/**
 * The classic identity function.
 *
 * "... an identity function ... is a function that always returns
 * the same value that was used as its argument."
 *
 * https://en.wikipedia.org/wiki/Identity_function
 *
 * @param {any} x - The value to identify
 * @returns {any} The value that was passed
 */
export default function identity(x) {
	return x;
}
